function Button({
  className,
  variant = "default",
  size = "default",
  asChild = false,
  ...props
}: React.ComponentProps<"button"> &
  VariantProps<typeof buttonVariants> & {
    asChild?: boolean
  }) {
  const Comp = asChild ? Slot.Root : "button"

  return (
    <Comp
      data-slot="button"
      data-variant={variant}
      data-size={size}
      className={cn(buttonVariants({ variant, size, className }))}
      {...props}
    />
  )
}

export { Button, buttonVariants }

// ---- Project OS additions -------------------------------------------------

import Link from "next/link"
import { motion, useReducedMotion } from "framer-motion"

/** Anchor-styled button using next/link, with a barely-there hover scale. */
function LinkButton({
  variant = "primary",
  size = "md",
  className = "",
  children,
  ...rest
}: React.ComponentProps<typeof Link> &
  VariantProps<typeof buttonVariants> & { children: React.ReactNode }) {
  const reduce = useReducedMotion()
  return (
    <motion.div
      className="inline-flex"
      whileHover={reduce ? undefined : { scale: 1.015 }}
      whileTap={reduce ? undefined : { scale: 0.985 }}
      transition={{ duration: 0.18, ease: [0.22, 1, 0.36, 1] }}
    >
      <Link className={cn(buttonVariants({ variant, size, className }))} {...rest}>
        {children}
      </Link>
    </motion.div>
  )
}

export { LinkButton }
